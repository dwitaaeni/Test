public class OperasiMatematika {
    public static void main(String[] args) {

        int a = 110;
        int b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a % b);
        System.out.println(a / b);

        // Augmented Assigments
        int c = 120;

        c += 10;
        System.out.println(c);

        c -= 10;
        System.out.println(c);

        c *= 10;
        System.out.println(c);

        // Unary Operator
        int d = 110;

        d++;
        System.out.println(d);

        d--;
        System.out.println(d);

    }
}
