public class OperasiPerbandingan {
    public static void main(String[] args) {

        int a = 112;
        int b = 133;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
